# Um bestimmte Zeichen in einem Print auszugeben müssen wir diverse Zeichen maskieren.
# Maskieren ist ganz einfach. Man setzt einfach ein \ vor dem Zeichen was geprinted werden soll.

print("Backslashes \\ and single quotes \' and double quotes \" and pound signs # are awesome!")

# Die Ausgabe von diesem Print wäre: Backslashes \ and single quotes ' and double quotes " and pound signs # are awesome!
